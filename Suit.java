/**
 * An enumeration representing the suit of a card in a Blackjack game.
 * 
 * This enum defines the four standard suits used in playing cards. 
 */
public enum Suit {

  /** The suit of Spades. */
  SPADES,

  /** The suit of Hearts. */
  HEARTS,

  /** The suit of Diamonds. */
  DIAMONDS,

  /** The suit of Clubs. */
  CLUBS
}